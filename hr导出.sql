  select p.pid,p.product_id,b.p_name,p.ORDER_ID,
p.NUM as num_out,b.price as price_in,
p.PRICE as price_out,
(p.PRICE-b.price)*p.num as profit
from products_part2 p,PRODUCT_BASE b
where p.product_id=b.product_id
